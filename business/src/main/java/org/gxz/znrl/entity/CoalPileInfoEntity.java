public class CoalPileInfoEntity {

    private  String coalPileName;
    private  String coalType;
    private  String coalHot;
    private  String coalHeapQty;
    private  String coalStad;
    private  String coalVad;
    private  String coalPrice;
    private  String mcCode;

    public String getCoalPileName() {
        return coalPileName;
    }

    public void setCoalPileName(String coalPileName) {
        this.coalPileName = coalPileName;
    }

    public String getCoalType() {
        return coalType;
    }

    public void setCoalType(String coalType) {
        this.coalType = coalType;
    }

    public String getCoalHot() {
        return coalHot;
    }

    public void setCoalHot(String coalHot) {
        this.coalHot = coalHot;
    }

    public String getCoalHeapQty() {
        return coalHeapQty;
    }

    public void setCoalHeapQty(String coalHeapQty) {
        this.coalHeapQty = coalHeapQty;
    }

    public String getCoalStad() {
        return coalStad;
    }

    public void setCoalStad(String coalStad) {
        this.coalStad = coalStad;
    }

    public String getCoalVad() {
        return coalVad;
    }

    public void setCoalVad(String coalVad) {
        this.coalVad = coalVad;
    }

    public String getCoalPrice() {
        return coalPrice;
    }

    public void setCoalPrice(String coalPrice) {
        this.coalPrice = coalPrice;
    }

    public String getMcCode() {
        return mcCode;
    }

    public void setMcCode(String mcCode) {
        this.mcCode = mcCode;
    }




}
